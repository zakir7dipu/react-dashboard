$(document).ready(function () {

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION -----------------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $.notify("Make sure to put the delay value to 0 when using the blur effect", {type:"info" ,close:true,delay:0});

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION DEFAULT ---------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $("#notification-default").click(function () {
        $.notify("Default !", {type:"default"});
    });

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION TOAST -----------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $("#notification-toast").click(function () {
        $.notify("Toast !", {type:"toast"});
    });

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION SUCCESS ---------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $("#notification-success").click(function () {
        $.notify("Success !", {type:"success"});
    });

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION INFO ------------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $("#notification-info").click(function () {
        $.notify("Info !", {type:"info"});
    });

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION WARNING ---------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $("#notification-warning").click(function () {
        $.notify("Warning !", {type:"warning"});
    });

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION DANGER ----------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $("#notification-danger").click(function () {
        $.notify("Danger !", {type:"danger"});
    });

    //---------------------------------------------------------------------------------------------
    // - NOTIFICATION CUSTOM ----------------------------------------------------------------------
    //---------------------------------------------------------------------------------------------

    $("#notification-custom").click(function () {
        var text = $('#notification-text').val();
        var options = {};
        options.align = $('#notification-align').val();
        options.verticalAlign = $('#notification-verticalAlign').val();
        options.delay = $('#notification-delay').val();
        options.animationType = $('#notification-animationType').val();
        options.color = $('#notification-color').val();
        options.background = $('#notification-background').val();
        options.icon = $('#notification-icon').val();
        options.blur = parseFloat($('#notification-blur').val());
        options.close = $('#notification-close').val();
        $.notify(text, options);
    });

});
